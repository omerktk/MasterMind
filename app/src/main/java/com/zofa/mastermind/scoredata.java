package com.zofa.mastermind;

public class scoredata {
    String name,score;

    public scoredata() {
    }

    public scoredata(String name, String score) {
        this.name = name;
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }
}
